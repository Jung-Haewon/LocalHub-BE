def parse_question(question):

    district = None
    contenttype = None

    keywords = []

    for r in DISTRICTS:
        if r in question:
            district = r

    for key, value in CONTENT_TYPES.items():
        if key in question:
            contenttype = value

    words = question.split()

    for w in words:

        if w not in DISTRICTS and w not in CONTENT_TYPES:
            keywords.append(w)

    return {
        "district": district,
        "contenttype": contenttype,
        "keywords": keywords
    }